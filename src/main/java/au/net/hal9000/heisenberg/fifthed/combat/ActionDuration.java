package au.net.hal9000.heisenberg.fifthed.combat;

/** Durations within a player's single round. */
public enum ActionDuration {
  /** Represents a full-round action. */
  FULL_ROUND,
  /** Represents a standard action. */
  STANDARD,
  /** Represents a move action. */
  MOVE,
  /** Represents a five-foot step action. */
  FIVE_FOOT_STEP,
  /** Represents a swift action. */
  SWIFT,
  /** Represents an immediate action. */
  IMMEDIATE,
  /** Represents a free action. */
  FREE,
}
